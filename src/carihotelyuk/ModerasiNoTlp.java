    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        int dialogResult=JOptionPane.showOptionDialog(null,"Apakah anda yakin ingin menolak No Tlp untuk hotel ini?","Konfirmasi Tolak dan Otomatis terhapus Data Hotel",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE,null,null,null);
        if(dialogResult==JOptionPane.YES_OPTION){
            int rowIdx=jtabDaftarModerasiNoTlp.getSelectedRow();
            String no_telp=jtabDaftarModerasiNoTlp.getValueAt(rowIdx,0).toString();
            System.out.println(no_telp);
            try{
                String sql="DELETE FROM no_telp WHERE no_telp='"+no_telp+"'";
                Connection con=ModuleDB.connectDB();
                Statement stmt=con.createStatement();
                stmt.executeUpdate(sql);
                showMessageDialog(null,"No Tlp telah ditolak!");
                daftarNoTlp();
                con.close();
            }
            catch(SQLException e){
                showMessageDialog(null,e.getMessage());
            }
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

    private void buttonSetujuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSetujuActionPerformed
        int rowIdx=jtabDaftarModerasiNoTlp.getSelectedRow();
        String no_telp=jtabDaftarModerasiNoTlp.getValueAt(rowIdx,0).toString();
        String status= "approved";
        try{
            Connection con=ModuleDB.connectDB();
            Statement stmt=con.createStatement();
            String sql="UPDATE no_telp SET status_moderasi='"+status+"' WHERE no_telp='"+no_telp+"'";
            stmt.executeUpdate(sql);
            showMessageDialog(null,"No Tlp telah di APPROVE!");
            daftarNoTlp();
            con.close();
            
        }
        catch(SQLException e){
            showMessageDialog(null,e.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonSetujuActionPerformed

