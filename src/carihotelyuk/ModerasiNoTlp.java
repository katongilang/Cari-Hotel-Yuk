    private void daftarNoTlp(){
        try{
            String sql = "SELECT n.no_telp,n.id_user,n.id_hotel,n.status_moderasi,h.id_hotel,h.nama_hotel,u.id_user,u.nama_lengkap FROM hotel h, no_telp n, user u WHERE n.status_moderasi='waiting' AND n.id_hotel=h.id_hotel AND n.id_user=u.id_user";
            Connection con=ModuleDB.connectDB();
            Statement stmt=con.createStatement();
            ResultSet result=stmt.executeQuery(sql);
            DefaultTableModel model=(DefaultTableModel)jtabDaftarModerasiNoTlp.getModel();
            model.setRowCount(0);
            while(result.next()){
                Vector row=new Vector();
                row.add(result.getString("no_telp"));
                row.add(result.getString("nama_hotel"));                
                row.add(result.getString("nama_lengkap"));
                row.add(result.getString("status_moderasi"));
                model.addRow(row);
            }
            con.close();
            jtabDaftarModerasiNoTlp.setModel(model);
        }
        catch(SQLException e){
            showMessageDialog(null,e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
