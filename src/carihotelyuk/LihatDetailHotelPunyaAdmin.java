    private void btnDeleteRuanganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRuanganActionPerformed
        int dialogResult=JOptionPane.showOptionDialog(null,"Apakah anda yakin menghapus ruangan hotel ini?","Konfirmasi Hapus Data Hotel",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE,null,null,null);
        if(dialogResult==JOptionPane.YES_OPTION){
            int rowIdx=jtabRuangan.getSelectedRow();
            int idRuangan=Integer.parseInt(jtabRuangan.getValueAt(rowIdx,0).toString());
            String sql="DELETE FROM ruangan WHERE id_ruang="+idRuangan;
            try{
                Connection con=ModuleDB.connectDB();
                Statement stmt=con.createStatement();
                stmt.executeUpdate(sql);
                showMessageDialog(null,"Ruangan berhasil dihapus!");
                tampilanRuangan();
            }
            catch(SQLException e){
                showMessageDialog(null,e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteRuanganActionPerformed

