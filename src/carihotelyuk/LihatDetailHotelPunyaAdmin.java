    public void tampilanRuangan(){
        int rowIdx=jtabRuangan.getSelectedRow();
        //int idHotelLihatRuangan=Integer.parseInt(jtabRuangan.getValueAt(rowIdx,0).toString());
        try{
            String sql="SELECT h.id_hotel, h.nama_hotel,r.id_hotel,r.kelas_ruang,r.id_ruang,r.kapasitas,r.harga_per_malam FROM hotel h, ruangan r WHERE r.id_hotel=h.id_hotel AND h.id_hotel="+idHotel;
            Connection con=ModuleDB.connectDB();
            Statement stmt=con.createStatement();
            ResultSet result=stmt.executeQuery(sql);
            DefaultTableModel model=(DefaultTableModel)jtabRuangan.getModel();
            model.setRowCount(0);
            while(result.next()){
                Vector row=new Vector();
                row.add(result.getString("id_ruang"));
                row.add(result.getString("nama_hotel"));
                row.add(result.getString("kelas_ruang"));
                row.add(result.getString("kapasitas"));
                row.add(result.getInt("harga_per_malam"));
                model.addRow(row);
            }
            con.close();
            jtabRuangan.setModel(model);
        }
        catch(SQLException e){
            showMessageDialog(null,e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    public void tampilanNoTlp(){
        int rowIdx=jtabDaftarNoTlp.getSelectedRow();
       
        try{
            String sql="SELECT h.id_hotel,n.no_telp,n.status_moderasi FROM hotel h, no_telp n WHERE n.id_hotel=h.id_hotel AND n.status_moderasi='approved' AND h.id_hotel="+idHotel;
            Connection con=ModuleDB.connectDB();
            Statement stmt=con.createStatement();
            ResultSet result=stmt.executeQuery(sql);
            DefaultTableModel model=(DefaultTableModel)jtabDaftarNoTlp.getModel();
            model.setRowCount(0);
            while(result.next()){
                Vector row=new Vector();
                row.add(result.getString("no_telp"));
                row.add(result.getString("status_moderasi"));
                model.addRow(row);
            }
            con.close();
            jtabDaftarNoTlp.setModel(model);
        }
        catch(SQLException e){
            showMessageDialog(null,e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
