    private void hpsUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hpsUserActionPerformed
        int dialogResult=JOptionPane.showOptionDialog(null,"Apakah anda yakin menghapus user ini?","Konfirmasi Hapus Data Hotel",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE,null,null,null);
        if(dialogResult==JOptionPane.YES_OPTION){
            int rowIdx=jtabUser.getSelectedRow();
            int idUser=Integer.parseInt(jtabUser.getValueAt(rowIdx,0).toString());
            String sql="DELETE FROM user WHERE id_user="+idUser;
            try{
                Connection con=ModuleDB.connectDB();
                Statement stmt=con.createStatement();
                stmt.executeUpdate(sql);
                showMessageDialog(null,"User berhasil dihapus!");
                tampilkan();
                con.close();
            }
            catch(SQLException e){
                showMessageDialog(null,e.getMessage());
            }
        }
    }//GEN-LAST:event_hpsUserActionPerformed

