    private void tampilkan(){
        try{
            String sql= "SELECT * FROM user";
            Connection con=ModuleDB.connectDB();
            Statement stmt=con.createStatement();
            ResultSet result=stmt.executeQuery(sql);
            DefaultTableModel model=(DefaultTableModel)jtabUser.getModel();
            model.setRowCount(0);
            while(result.next()){
                Vector row=new Vector();
                row.add(result.getInt("id_user"));
                row.add(result.getString("nama_lengkap"));
                row.add(result.getString("email"));
                model.addRow(row);
            }
            con.close();
            jtabUser.setModel(model);
        }
        catch(SQLException e){
            showMessageDialog(null,e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
